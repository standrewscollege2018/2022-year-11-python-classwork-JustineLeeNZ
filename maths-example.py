# maths in python
#
num1 = 15
num2 = 7

# addition
result = num1 + num2
print(f"Addition: {result}")


#subtraction
result = num1 - num2
print(f"Subtraction: {result}")

#multiplication
result = num1 * num2
print(f"Multiplication: {result}")

#division
result = num1 / num2
print(f"Division: {result}")

#floor division
result = num1 // num2
print(f"{num2} goes into {num1} {result} times")

# modulo - division - only remainder
result = num1 % num2
print(f"Dividing {num1} by {num2} gives the remainder {result}")




