# create a list of cars
car_list = ["Ford", "Honda", "Tesla", "Ferrari", "Subaru", "Porsche"]

# use temporary variable in for loop to print list
for car in car_list:
    print(f"This car is a {car}")

# get length of list
length = len(car_list)
print(length)

# use range with for loop to print numbered list
for i in range(0,len(car_list)):
    print(car_list[i])

